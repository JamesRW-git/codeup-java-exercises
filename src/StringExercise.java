public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education\n" + "We don't need no thought control";
        String checkOut = "Check \"this\" out!, \"s inside of \"s!";
        String bindows = "In windows, the main drive is usually C:\\";
        String backslashes = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(education);
        System.out.println(checkOut);
        System.out.println(bindows);
        System.out.println(backslashes);
    }
}
